        VkFormat operator[](TEXTURE_FORMAT TexFmt) const
        {
            u64 lkupVal = u64(TexFmt);
            WB_ASSERT(lkupVal < _countof(lookup))(lkupVal, _countof(lookup));
            return lookup[lkupVal];
        }

    private:
        void set(TEXTURE_FORMAT k, VkFormat v) {
            lookup[u64(k)] = v;
        }

